var apiNameIndex = func() map[string]int32 {
	m := make(map[string]int32, len(apiNames))
	for i, name := range apiNames {
		m[name] = int32(i)
	}
	return m
}()

// apiIndexFromName returns the ApiNameEnum index for an API name, port of
// getApiFromPath.
func apiIndexFromName(name string) (int32, bool) {
	idx, ok := apiNameIndex[name]
	return idx, ok
}
